# ==================== Клавиатуры ====================
def get_main_keyboard(user_id):
    markup = InlineKeyboardMarkup(row_width=1)
    completed = user_progress.get(user_id, 0)

    for i in range(1, 7):
        status = " ✅" if i <= completed else ""
        if i <= completed + 1:
            markup.add(InlineKeyboardButton(f"ТЕОРИЯ {i}{status}", callback_data=f"module_{i}"))
        else:
            markup.add(InlineKeyboardButton(f"ТЕОРИЯ {i} 🔒", callback_data="locked"))

    if completed >= 6:
        markup.add(InlineKeyboardButton("📝 ПРОЙТИ ТЕСТ", callback_data="take_test"))

    return markup


# ==================== Обработчики ====================
@bot.message_handler(commands=['start'])
def start(message):
    user_id = message.chat.id
    if user_id not in user_progress:
        user_progress[user_id] = 0

    text = "Добро пожаловать в обучающий курс!\n\nВыберите модуль. Тест откроется после прохождения всех модулей."

    bot.send_message(message.chat.id, text, reply_markup=get_main_keyboard(user_id))


@bot.callback_query_handler(func=lambda call: call.data.startswith("module_"))
def handle_module(call):
    user_id = call.message.chat.id
    module_num = int(call.data.split("_")[1])
    completed = user_progress.get(user_id, 0)

    if module_num > completed + 1:
        bot.answer_callback_query(call.id, "Сначала пройдите предыдущие модули!", show_alert=True)
        return

    markup = InlineKeyboardMarkup()
    markup.add(InlineKeyboardButton("✅ Я прошёл модуль", callback_data=f"complete_{module_num}"))

    bot.edit_message_text(
        chat_id=call.message.chat.id,
        message_id=call.message.message_id,
        text=modules[module_num],
        parse_mode="HTML",
        reply_markup=markup
    )
    bot.answer_callback_query(call.id)


@bot.callback_query_handler(func=lambda call: call.data.startswith("complete_"))
def complete_module(call):
    user_id = call.message.chat.id
    module_num = int(call.data.split("_")[1])
    completed = user_progress.get(user_id, 0)

    if module_num == completed + 1:
        user_progress[user_id] = module_num

        bot.edit_message_text(
            chat_id=call.message.chat.id,
            message_id=call.message.message_id,
            text="Добро пожаловать в обучающий курс!\n\nВыберите модуль. Тест откроется после прохождения всех модулей.",
            reply_markup=get_main_keyboard(user_id)
        )
    bot.answer_callback_query(call.id, f"Модуль {module_num} пройден ✅")


@bot.callback_query_handler(func=lambda call: call.data == "take_test")
def start_test(call):
    user_id = call.message.chat.id
    user_test_answers[user_id] = []
    send_test_question(call.message.chat.id, 0)


def send_test_question(chat_id, question_index):
    if question_index >= len(test_questions):
        # Финальное сообщение после теста
        final_text = "🎉 <b>Вы успешно прошли тестирование!</b>\n\nУведомите об этом своего куратора."
        bot.send_message(chat_id, final_text, parse_mode="HTML")
        return

    markup = InlineKeyboardMarkup(row_width=1)
    for i, option in enumerate(test_options[question_index]):
        markup.add(InlineKeyboardButton(option, callback_data=f"test_{question_index}_{i}"))

    bot.send_message(
        chat_id,
        f"<b>Вопрос {question_index + 1} из {len(test_questions)}</b>\n\n{test_questions[question_index]}",
        parse_mode="HTML",
        reply_markup=markup
    )


@bot.callback_query_handler(func=lambda call: call.data.startswith("test_"))
def handle_test_answer(call):
    user_id = call.message.chat.id
    _, q_index, _ = call.data.split("_")
    q_index = int(q_index)

    bot.answer_callback_query(call.id)

    # Переходим к следующему вопросу
    send_test_question(call.message.chat.id, q_index + 1)


